package com.netcommlabs.sarofficenet.model;

public class NotesModel {
    String Date,FullNotes,NotesID,ShortNotes;

    public String getDate() {
        return Date;
    }

    public void setDate(String date) {
        Date = date;
    }

    public String getFullNotes() {
        return FullNotes;
    }

    public void setFullNotes(String fullNotes) {
        FullNotes = fullNotes;
    }

    public String getNotesID() {
        return NotesID;
    }

    public void setNotesID(String notesID) {
        NotesID = notesID;
    }

    public String getShortNotes() {
        return ShortNotes;
    }

    public void setShortNotes(String shortNotes) {
        ShortNotes = shortNotes;
    }
}
